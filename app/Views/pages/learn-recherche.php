                    <p class="mb-4" style="font-family: 'Century Gothic'!important; text-align: justify;">
                        La Faculté de Médecine de Kamina accorde une place centrale à la recherche scientifique, pilier fondamental de la formation médicale et de l’innovation en santé.
                        Elle encourage les enseignants et les étudiants à développer des travaux orientés vers la résolution des problèmes sanitaires prioritaires de la République Démocratique du Congo.
                        <br>
                        <br>
                        Les enseignants-chercheurs de la Faculté mènent diverses études dans les domaines suivants :

                        Santé maternelle et infantile, notamment sur la mortalité néonatale, la santé reproductive et la nutrition des mères et des enfants ;

                        Vaccination et maladies évitables par la vaccination, avec un accent particulier sur les stratégies de couverture vaccinale et l’hésitation vaccinale ;

                        Épidémiologie des maladies infectieuses, incluant le paludisme, la tuberculose, le VIH/SIDA et les épidémies émergentes comme Ebola et la COVID-19 ;

                        Santé environnementale et santé communautaire, portant sur l’accès à l’eau potable, l’assainissement, l’exposition aux pesticides et la prévention des maladies liées à l’environnement ;

                        Santé des travailleurs et pathologies respiratoires, notamment chez les mineurs exposés aux particules fines ;

                        Études cliniques et biomédicales, axées sur la physiopathologie, la pharmacologie et les biomarqueurs dans le diagnostic précoce des maladies.
                        <br>
                        <br>
                        La Faculté met à la disposition de ses chercheurs et étudiants un cadre propice à la recherche appliquée, grâce à ses laboratoires, à ses partenariats avec l’Université de Lubumbashi et aux collaborations avec les structures hospitalières locales.

                        Les résultats issus de ces recherches contribuent à l’amélioration des politiques de santé, au renforcement des capacités médicales locales et à la production de connaissances scientifiques utiles au développement national.

                        La Faculté de Médecine de Kamina s’impose ainsi comme un centre d’excellence scientifique régional, orienté vers la recherche de solutions durables aux défis sanitaires du pays.
